/**
 * @file approx_t.h
 * @brief Структура, представляющая приблизительное число.
 *
 * Приблизительное число представляет собой пару чисел, представляющих нижнюю и верхнюю
 * границы числа.
 */
#define APPROX_H

#include <stdbool.h>
#include <stdio.h>
 /**
  * @struct approx_t
  * @brief Структура, представляющая приблизительное число.
  *
  * @var approx_t::lo
  * Нижняя граница числа.
  *
  * @var approx_t::hi
  * Верхняя граница числа.
  */
typedef struct {
    double lo;//левая граница
    double hi;//правая граница
} approx_t;
/**
 * @brief Создает приблизительное число из центра и радиуса.
 *
 * @param center Центр приближенного числа.
 * @param radius Радиус приближенного числа.
 *
 * @return Приближенное число с заданными центром и радиусом.
 */
approx_t ApproxFromDouble(double center, double radius);
/**
 * @brief Создает приближенное число из левой и правой границы.
 *
 * @param lo Левая граница приближенного числа.
 * @param hi Правая граница приближенного числа.
 *
 * @return Приближенное число с указанными левой и правой границами.
 */
approx_t ApproxFromRange(double value);
/**
 * @brief Уничтожение приближенного числа.
 *
 * @param approx Приблизительное число для уничтожения.
 */
void ApproxDestroy(approx_t* approx);
/**
 * @brief Получает центр приблизительного числа.
 *
 * @param approx Приблизительное число, центр которого нужно получить.
 *
 * @return Центр приблизительного числа.
 */
double ApproxAsDouble(approx_t approx);
/**
 * @brief Получает радиус приближенного числа.
 *
 * @param approx Приблизительное число для получения радиуса.
 *
 * @return Радиус приближенного числа.
 */
double ApproxGetRadius(approx_t approx);
/**
 * @brief Разбирает строку и возвращает соответствующее приблизительное число.
 *
 * @param str Строка для разбора.
 *
 * @return Приблизительное число, соответствующее строке, или приблизительное
 * число с границами (0.0, 0.0), если строка не может быть разобрана.
 */
approx_t ApproxParse(const char* str);
/**
 * @brief Форматирует приблизительное число в виде строки.
 *
 * @param approx Приблизительное число для форматирования.
 * @param buf Буфер для записи отформатированного приблизительного числа.
 * @param bufsize Размер буфера.
 *
 * @return Указатель на отформатированное приблизительное число в буфере.
 */
char* ApproxFormat(approx_t approx, char* buf, size_t bufsize);
/**
 * @brief Считывание приблизительного числа из файла.
 *
 * @param fp Файл для чтения из файла.
 * @param approx Приблизительное число для чтения.
 *
 * @return 1, если приблизительное число было считано успешно, 0, если был достигнут конец файла, или -1, если был достигнут конец файла.
 * файл был достигнут, или -1, если произошла ошибка.
 */
int ApproxRead(approx_t* approx, FILE* fp);
/**
 * @brief Записывает приблизительное число в файл.
 *
 * @param approx Приблизительное число для записи.
 * @param fp Файл для записи.
 *
 * @return 1, если приблизительное число было записано успешно, 0, если произошла ошибка.
 * произошла.
 */
int ApproxWrite(approx_t approx, FILE* fp);
/**
 * @brief Складывает два приближенных числа.
 *
 * @param a Первое приближенное число.
 * @param b Второе приближенное число.
 *
 * @return Сумма двух приближенных чисел.
 */
approx_t ApproxAdd(approx_t a, approx_t b);
/**
 * @brief Вычитание двух приближенных чисел.
 *
 * @param a Первое приближенное число.
 * @param b Второе приближенное число.
 *
 * @return Разность двух приближенных чисел.
 */
approx_t ApproxSub(approx_t a, approx_t b);
/**
 * @brief Перемножает два приближенных числа.
 *
 * @param a Первое приближенное число.
 * @param b Второе приближенное число.
 * @return Произведение двух приближенных чисел
 */
approx_t ApproxMul(approx_t a, approx_t b);
/**
 * @brief Делит два приближенных числа.
 *
 * @param a Первое приближенное число.
 * @param b Второе приближенное число.
 *
 * @return Коэффициент двух приближенных чисел.
 */
approx_t ApproxDiv(approx_t a, approx_t b);
/**
 * @brief Отрицание приближенного числа.
 *
 * @param approx Приблизительное отрицательоне число.
 *
 * @return Отрицание приблизительного числа.
 */
approx_t ApproxNegate(approx_t approx);
/**
 * @brief Получает абсолютное значение приближенного числа.
 *
 * @param approx Приблизительное число, для которого нужно получить абсолютное значение.
 *
 * @return Абсолютное значение приблизительного числа.
 */
approx_t ApproxAbs(approx_t approx);
/**
 * @brief Сравнивает два приближенных числа.
 *
 * @param a Первое приближенное число.
 * @param b Второе приближенное число.
 *
 * @return -1, если a < b, 0, если a == b, и 1, если a > b.
 */
int ApproxCompare(approx_t a, approx_t b);


